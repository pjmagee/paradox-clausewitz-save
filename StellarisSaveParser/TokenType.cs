
public enum TokenType
{
    /// <summary>
    /// A string identifier.
    /// </summary>
    Identifier,
    /// <summary>
    /// A string literal.
    /// </summary>
    StringLiteral,
    /// <summary>
    /// A number literal.
    /// </summary>
    NumberLiteral,
    /// <summary>
    /// An equals sign.
    /// </summary>
    Equals,
    /// <summary>
    /// A curly brace open.
    /// </summary>
    CurlyOpen,
    /// <summary>
    /// A curly brace close.
    /// </summary>
    CurlyClose,
    /// <summary>
    /// A square brace open.
    /// </summary>
    SquareOpen,
    /// <summary>
    /// A square brace close.
    /// </summary>
    EndOfFile
}
